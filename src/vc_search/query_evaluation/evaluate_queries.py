class QueryEvaluator:
    def __init__(self):
        self.es = VCElasticSearch()

    def clean_content(self, content, max_length=32000):
        """–£–±–∏—Ä–∞–µ—Ç –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ –∏–∑ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –∏ –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ—Ç –¥–ª–∏–Ω—É"""
        if not content:
            return ""
        # –ó–∞–º–µ–Ω—è–µ–º –≤—Å–µ –≤–∏–¥—ã –ø–µ—Ä–µ–Ω–æ—Å–æ–≤ —Å—Ç—Ä–æ–∫ –ø—Ä–æ–±–µ–ª–∞–º–∏ –∏ —É–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã
        cleaned = content.replace("\n", " ").replace("\r", " ").replace("\t", " ")
        # –£–±–∏—Ä–∞–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã
        normalized = " ".join(cleaned.split())

        # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –¥–ª–∏–Ω—É –¥–ª—è CSV —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏
        if len(normalized) > max_length:
            return normalized[:max_length] + "... [—Ç–µ–∫—Å—Ç –æ–±—Ä–µ–∑–∞–Ω]"
        return normalized

    def search_queries(self, queries, results_per_query=10, output_file=None):
        """–ò—â–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –¥–ª—è —Å–ø–∏—Å–∫–∞ –∑–∞–ø—Ä–æ—Å–æ–≤ –∏—Å–ø–æ–ª—å–∑—É—è improved_search –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç –≤ CSV"""
        if not output_file:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            output_file = f"search_results_{timestamp}.csv"

        all_results = []

        for query in queries:
            print(f"üîç Searching: '{query}'")

            try:
                # –ò—Å–ø–æ–ª—å–∑—É–µ–º improved_search –∏–∑ VCElasticSearch
                search_results = self.es.improved_search(query, limit=results_per_query)

                for i, result in enumerate(search_results["results"]):
                    # –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª–Ω—ã–π –∫–æ–Ω—Ç–µ–Ω—Ç —Å—Ç–∞—Ç—å–∏ –ø–æ ID
                    full_content = self.get_full_article_content_by_id(
                        result.get("id", "")
                    )

                    # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ –ø–æ ID, –ø—Ä–æ–±—É–µ–º –ø–æ–ª—É—á–∏—Ç—å –∫–æ–Ω—Ç–µ–Ω—Ç –∏–∑ –ø—Ä–µ–≤—å—é
                    if not full_content:
                        full_content = result.get("content", "")

                    result_data = {
                        "relevance_score": "",  # –¥–ª—è —Ä—É—á–Ω–æ–π —Ä–∞–∑–º–µ—Ç–∫–∏
                        "query": query,
                        "article_title": self.clean_content(result.get("title", "")),
                        "article_category": result.get("section", ""),
                        "article_content": self.clean_content(
                            full_content
                        ),  # –ø–æ—Å–ª–µ–¥–Ω–∏–π —Å—Ç–æ–ª–±–µ—Ü
                    }
                    all_results.append(result_data)

                print(f"‚úÖ Found {len(search_results['results'])} results")

            except Exception as e:
                print(f"‚ùå Error for query '{query}': {e}")
                continue

        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ CSV
        self._save_to_csv(all_results, output_file)
        return output_file

    def get_full_article_content_by_id(self, article_id):
        """–ü–æ–ª—É—á–∞–µ—Ç –ø–æ–ª–Ω—ã–π –∫–æ–Ω—Ç–µ–Ω—Ç —Å—Ç–∞—Ç—å–∏ –ø–æ ID –∏–∑ Elasticsearch"""
        try:
            # –ü–æ–ª—É—á–∞–µ–º –¥–æ–∫—É–º–µ–Ω—Ç –ø–æ ID
            response = self.es.client.get(index=self.es.index_name, id=article_id)

            if response["found"]:
                full_content = response["_source"].get("content", "")
                print(
                    f"üìÑ –ü–æ–ª—É—á–µ–Ω –∫–æ–Ω—Ç–µ–Ω—Ç –¥–ª–∏–Ω–æ–π {len(full_content)} —Å–∏–º–≤–æ–ª–æ–≤ (ID: {article_id})"
                )
                return full_content
            else:
                print(f"‚ö†Ô∏è –°—Ç–∞—Ç—å—è –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –ø–æ ID: {article_id}")
                return ""

        except Exception as e:
            print(f"‚ö†Ô∏è Could not fetch content by ID {article_id}: {e}")
            return ""

    def _save_to_csv(self, results, output_file):
        """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤ CSV —Ñ–∞–π–ª"""
        if not results:
            print("‚ùå No results to save")
            return

        # –°—Ç–æ–ª–±—Ü—ã –≤ –Ω—É–∂–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ - article_content –ø–æ—Å–ª–µ–¥–Ω–∏–π
        fieldnames = [
            "relevance_score",
            "query",
            "article_title",
            "article_category",
            "article_content",  # –ø–æ—Å–ª–µ–¥–Ω–∏–π —Å—Ç–æ–ª–±–µ—Ü
        ]

        try:
            with open(output_file, "w", newline="", encoding="utf-8") as csvfile:
                writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
                writer.writeheader()

                for result in results:
                    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–ª–∏–Ω—É –∫–∞–∂–¥–æ–≥–æ –ø–æ–ª—è –ø–µ—Ä–µ–¥ –∑–∞–ø–∏—Å—å—é
                    cleaned_result = {}
                    for key, value in result.items():
                        if isinstance(value, str):
                            # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –æ—á–∏—Å—Ç–∫–∞ –¥–ª—è CSV
                            cleaned_value = value.replace('"', "'").replace(";", ",")
                            cleaned_result[key] = cleaned_value
                        else:
                            cleaned_result[key] = value

                    writer.writerow(cleaned_result)

            print(f"üíæ Results saved to: {output_file}")
            print(f"üìä Total records: {len(results)}")

            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –∫–æ–Ω—Ç–µ–Ω—Ç –≤ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
            content_stats = self._check_content_stats(results)
            print(f"üìù Content statistics: {content_stats}")

        except Exception as e:
            print(f"‚ùå Error saving CSV: {e}")
            # –ü—Ä–æ–±—É–µ–º —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –±–µ–∑ –ø—Ä–æ–±–ª–µ–º–Ω—ã—Ö —Å—Ç—Ä–æ–∫
            self._save_fallback(results, output_file)

    def _check_content_stats(self, results):
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –∫–æ–Ω—Ç–µ–Ω—Ç—É"""
        total = len(results)
        with_content = 0
        content_lengths = []

        for result in results:
            content = result.get("article_content", "")
            if content and len(content.strip()) > 10:
                with_content += 1
                content_lengths.append(len(content))

        stats = {
            "total_articles": total,
            "articles_with_content": with_content,
            "avg_content_length": sum(content_lengths) // len(content_lengths)
            if content_lengths
            else 0,
        }
        return stats

    def _save_fallback(self, results, output_file):
        """–ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π –º–µ—Ç–æ–¥ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –µ—Å–ª–∏ –æ—Å–Ω–æ–≤–Ω–æ–π –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç"""
        try:
            with open(output_file, "w", newline="", encoding="utf-8") as csvfile:
                writer = csv.DictWriter(
                    csvfile,
                    fieldnames=[
                        "relevance_score",
                        "query",
                        "article_title",
                        "article_category",
                        "article_content",
                    ],
                )
                writer.writeheader()

                for i, result in enumerate(results):
                    try:
                        writer.writerow(result)
                    except Exception as row_error:
                        print(f"‚ö†Ô∏è Skipping row {i} due to error: {row_error}")
                        continue

            print(f"üíæ Results saved (fallback method): {output_file}")

        except Exception as e:
            print(f"‚ùå Critical error saving CSV: {e}")


